/**
 * Snapshots dist/index.html into api/_index-template.mjs so the OG serverless
 * function can serve the SPA shell with route-specific meta tags injected.
 *
 * Runs after `vite build` (see package.json). Vercel bundles api/ functions
 * after the build step, so the generated module is included in the deploy.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(here, '..');
const indexPath = path.join(root, 'dist', 'index.html');
const outPath = path.join(root, 'api', '_index-template.mjs');

if (!fs.existsSync(indexPath)) {
  console.error(`[inline-index-template] missing ${indexPath} — did vite build run?`);
  process.exit(1);
}

const html = fs.readFileSync(indexPath, 'utf-8');

const escaped = html
  .replace(/\\/g, '\\\\')
  .replace(/`/g, '\\`')
  .replace(/\$\{/g, '\\${');

const moduleSource = `// AUTO-GENERATED by scripts/inline-index-template.mjs. Do not edit by hand.
export const indexHtml = \`${escaped}\`;
`;

fs.writeFileSync(outPath, moduleSource, 'utf-8');
console.log(`[inline-index-template] wrote ${path.relative(root, outPath)} (${html.length} chars)`);
